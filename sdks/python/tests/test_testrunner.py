# ---------- doesn't leak to network ----------


def test_runner_does_not_use_real_transport():
    """The capture context must NOT leak captured runs into a real transport
    if one was previously initialized. Verify by checking that an httpx
    request would not have been made — we approximate this by asserting the
    capture object is gone after the context exits."""
    from safeship._config import get_config

    # Make sure no transport exists going in
    cfg = get_config()
    cfg._transport = None
    cfg.enabled = True

    def agent(msg: str) -> str:
        return "x"

    yaml_def = """
test: agent.dummy
when: step == "agent"
assert: output == "x"
"""
    entry = _entry("agent.dummy", yaml_def, replay_input="x")
    result = run_test(entry, agent)
    assert result.status == "passed"

    # After the runner returns, the transport should be back to None and
    # nothing should have been queued anywhere.
    assert cfg._transport is None
